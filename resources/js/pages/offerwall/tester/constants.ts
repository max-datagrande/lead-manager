export interface FieldConfig {
  token: string;
  label: string;
  possible_values: string[];
  default_value: string;
}

export interface CallLogData {
  id: number;
  status: 'success' | 'failed';
  http_status_code: number;
  duration_ms: number;
  request_url: string;
  request_method: string;
  request_headers: Record<string, string | string[]>;
  request_payload: Record<string, unknown>;
  response_headers: Record<string, string | string[]>;
  response_body: unknown;
  original_field_values: Record<string, unknown>;
  mapped_field_values: Record<string, unknown>;
}

export interface SectionResult {
  offers: Offer[];
  callLog: CallLogData | null;
  error?: string | null;
}

export interface Offer {
  pos: number;
  cpc: number | string;
  [key: string]: unknown;
}

export interface TestResults {
  results_by_cptype: Record<string, SectionResult>;
  meta: {
    mix_log_id: number;
    total_offers: number;
    successful_calls: number;
    failed_calls: number;
    duration_ms: number;
  };
}

export interface IntegrationOption {
  id: number;
  name: string;
  company: { id: number; name: string } | null;
}
